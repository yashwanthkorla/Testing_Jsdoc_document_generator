/**
 * Function to sum two numbers.
 *
 * @method
 *
 * @param {number} a
 * @param {number} b
 * @returns {number} gives sum of two numbers.
 */
const sum = (a, b) => a + b;

/**
 * Function to sub two numbers.
 *
 * @method
 *
 * @param {number} a
 * @param {number} b
 * @returns {number} gives sub of two numbers.
 */
const sub = (a, b) => a - b;

/**
 * Math operation tools.
 *
 * @module
 */
export const math = {
  sum,
  sub,
};
